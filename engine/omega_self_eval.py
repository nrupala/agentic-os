#!/usr/bin/env python3
"""
PHASE 12: Self-Evaluation Reporting
Generates periodic markdown reports on cognitive performance.
With zero-knowledge encryption.
"""

import json
from pathlib import Path
from datetime import datetime
from typing import Optional

try:
    from omega_phase_encryptor import OmegaPhaseEncryptor
    _ENCRYPTOR = OmegaPhaseEncryptor("self_eval")
    HAS_ZK = True
except ImportError:
    _ENCRYPTOR = None
    HAS_ZK = False

class SelfEvaluationReporting:
    def __init__(self, project_path: str):
        self.project_path = Path(project_path)
        self.eval_dir = self.project_path / "self-eval-logs"
        self.eval_dir.mkdir(parents=True, exist_ok=True)
    
    def generate_markdown_report(self, metrics: dict) -> str:
        """Generate comprehensive self-evaluation report."""
        
        timestamp = datetime.now().isoformat()
        
        report = f"""# OMEGA Self-Evaluation Report

**Generated:** {timestamp}  
**Project:** {self.project_path.name}

---

## Cognitive Assessment

### Recursion Depth
- Current: {metrics.get('recursion_depth', 0)}
- Max allowed: {metrics.get('max_recursion', 50)}
- Health: {self._health_status(metrics.get('recursion_depth', 0), metrics.get('max_recursion', 50))}

### Decision Logic
- Total decisions: {metrics.get('total_decisions', 0)}
- Correct decisions: {metrics.get('correct_decisions', 0)}
- Accuracy: {self._calc_accuracy(metrics)}%

### Discipline Audit
- Protocol violations: {metrics.get('violations', 0)}
- Auto-corrections: {metrics.get('auto_corrections', 0)}
- Success rate: {self._success_rate(metrics)}%

---

## Decision Log

{self._format_decisions(metrics.get('decisions', []))}

---

## Failure Patterns

{self._format_failures(metrics.get('failures', []))}

---

## Next Recursive Goal

{metrics.get('next_goal', 'Continue autonomous development')}

---

## Recommendations

{self._generate_recommendations(metrics)}

---

*Report generated by OMEGA Self-Evaluation System*
"""
        
        self._save_report(report, timestamp)
        return report
    
    def _health_status(self, current: int, max_allowed: int) -> str:
        """Calculate health status based on recursion depth."""
        ratio = current / max_allowed if max_allowed > 0 else 0
        
        if ratio < 0.5:
            return "[OK] HEALTHY"
        elif ratio < 0.8:
            return "[WARN] CAUTION"
        else:
            return "[CRIT] CRITICAL"
    
    def _calc_accuracy(self, metrics: dict) -> float:
        """Calculate decision accuracy percentage."""
        total = metrics.get('total_decisions', 0)
        correct = metrics.get('correct_decisions', 0)
        
        if total == 0:
            return 100.0
        
        return round((correct / total) * 100, 1)
    
    def _success_rate(self, metrics: dict) -> str:
        """Calculate overall success rate."""
        violations = metrics.get('violations', 0)
        corrections = metrics.get('auto_corrections', 0)
        
        if corrections == 0:
            return "100%"
        
        rate = (corrections / (violations + corrections)) * 100
        return f"{rate:.1f}%"
    
    def _format_decisions(self, decisions: list) -> str:
        """Format decision log entries."""
        if not decisions:
            return "*No decisions recorded*"
        
        lines = []
        for d in decisions[-10:]:
            status = "[OK]" if d.get('correct', False) else "[FAIL]"
            lines.append(f"- {status} {d.get('description', 'Unknown')}")
        
        return "\n".join(lines)
    
    def _format_failures(self, failures: list) -> str:
        """Format failure patterns."""
        if not failures:
            return "*No failures recorded*"
        
        lines = []
        for f in failures[-5:]:
            lines.append(f"### {f.get('category', 'Unknown')}")
            lines.append(f"- Occurrences: {f.get('count', 0)}")
            lines.append(f"- Last seen: {f.get('last_seen', 'Unknown')}")
            lines.append("")
        
        return "\n".join(lines)
    
    def _generate_recommendations(self, metrics: dict) -> str:
        """Generate actionable recommendations based on metrics."""
        recommendations = []
        
        accuracy = self._calc_accuracy(metrics)
        if accuracy < 80:
            recommendations.append("1. Review recent decision patterns for systematic errors")
        
        violations = metrics.get('violations', 0)
        if violations > 5:
            recommendations.append("2. Strengthen discipline protocol enforcement")
        
        depth = metrics.get('recursion_depth', 0)
        if depth > 30:
            recommendations.append("3. Consider increasing max_recursion or optimize logic")
        
        failures = metrics.get('failures', [])
        if len(failures) > 0:
            recommendations.append("4. Address failure patterns detected in recent runs")
        
        if not recommendations:
            recommendations.append("[OK] System performing optimally. Continue current approach.")
        
        return "\n".join(recommendations)
    
    def _save_report(self, report: str, timestamp: str):
        """Save report to self-eval-logs directory with optional encryption."""
        date = timestamp.split("T")[0]
        filename = f"{date}-{datetime.now().strftime('%H%M%S')}.md"
        filepath = self.eval_dir / filename
        
        if HAS_ZK and _ENCRYPTOR:
            try:
                payload = _ENCRYPTOR.encrypt_string(report)
                filepath.write_bytes(payload.nonce + payload.ciphertext)
                return
            except Exception:
                pass
        
        filepath.write_text(report)
    
    def get_latest_report(self) -> Optional[str]:
        """Get the most recent self-evaluation report."""
        reports = sorted(self.eval_dir.glob("*.md"), key=lambda p: p.stat().st_mtime)
        
        if reports:
            return reports[-1].read_text()
        
        return None


if __name__ == "__main__":
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: omega_self_eval.py <project_path>")
        sys.exit(1)
    
    project_path = sys.argv[1]
    evaluator = SelfEvaluationReporting(project_path)
    
    sample_metrics = {
        "recursion_depth": 15,
        "max_recursion": 50,
        "total_decisions": 42,
        "correct_decisions": 38,
        "violations": 2,
        "auto_corrections": 3,
        "decisions": [
            {"description": "Use JSON validation before parsing", "correct": True},
            {"description": "Retry with exponential backoff", "correct": True},
            {"description": "Skip error handling", "correct": False},
        ],
        "failures": [
            {"category": "import_error", "count": 3, "last_seen": "2026-04-15T10:30:00"},
        ],
        "next_goal": "Achieve 95% decision accuracy"
    }
    
    report = evaluator.generate_markdown_report(sample_metrics)
    print(report)
